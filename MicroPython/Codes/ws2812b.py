from ws2812lib import ws2812b
import time

# Создаем объект для управления светодиодом на пине GP23
led = ws2812b(pin=23)

# Пример 16 основных цветов:
# 0: Красный     (255, 0, 0)
# 1: Зеленый    (0, 255, 0)
# 2: Синий      (0, 0, 255)
# 3: Желтый     (255, 255, 0)
# 4: Циан       (0, 255, 255)
# 5: Магента    (255, 0, 255)
# 6: Белый      (255, 255, 255)
# 7: Черный     (0, 0, 0)
# 8: Оранжевый  (255, 165, 0)
# 9: Фиолетовый (148, 0, 211)
# 10: Лаванда   (230, 230, 250)
# 11: Розовый   (255, 192, 203)
# 12: Бежевый   (245, 245, 220)
# 13: Светло-зеленый (144, 238, 144)
# 14: Персиковый (255, 218, 185)
# 15: Лимонный  (255, 255, 102)

# В этом примере можно поиграть с цветами, меняя значения для set_pixel
# и с яркостью через метод brightness()

# Настроим яркость. По умолчанию яркость = 255 (максимальная)
led.brightness(37)  # Установим яркость на 12% (можно поиграться с этим значением)

# Функция для вывода разных цветов:
def show_colors():
    colors = [
        (255, 0, 0),         # Красный
        (0, 255, 0),         # Зеленый
        (0, 0, 255),         # Синий
        (255, 255, 0),       # Желтый
        (0, 255, 255),       # Циан
        (255, 0, 255),       # Магента
        (255, 255, 255),     # Белый
        (0, 0, 0),           # Черный
        (255, 165, 0),       # Оранжевый
        (148, 0, 211),       # Фиолетовый
        (230, 230, 250),     # Лаванда
        (255, 192, 203),     # Розовый
        (245, 245, 220),     # Бежевый
        (144, 238, 144),     # Светло-зеленый
        (255, 218, 185),     # Персиковый
        (255, 255, 102),     # Лимонный
    ]
    
    for color in colors:
        # Устанавливаем цвет на светодиоде
        led.set_pixel(color[0], color[1], color[2])  
        led.show()  # Применяем изменения
        time.sleep(1)  # Задержка 1 секунда для смены цвета

# Вызовем функцию для отображения всех цветов:
show_colors()

# Пример смены яркости во время смены цветов
for brightness in range(0, 256, 25):  # Изменяем яркость от 0 до 255 с шагом 51
    led.brightness(brightness)  # Устанавливаем новую яркость
    print(f"Яркость: {brightness}")  # Выводим текущую яркость
    show_colors()  # Показываем цвета с новой яркостью

