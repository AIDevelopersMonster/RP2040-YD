            while button.value():  # –ñ–¥—ë–º –Ω–∞–∂–∞—Ç–∏—è –∫–Ω–æ–ø–∫–∏
                time.sleep(0.1)
            
            if user_answer == correct_answer:
                print("‚úÖ –ü—Ä–∞–≤–∏–ª—å–Ω–æ!")
                show_color((0, 255, 0))  # –ó–µ–ª–µ–Ω—ã–π —Ü–≤–µ—Ç
